package javapackage;

public class operator2 {

	public static void main(String[] args) 
	{
		int a=20;
		System.out.println(a++); //a=20   21
		System.out.println(++a); //a=22   22

		System.out.println(a--);// a=22   21
		System.out.println(--a);//a=20    
		
		System.out.println(a);//a=20

	}

}
